#include "libft.h"

char	*ft_strdup(const char *s)
{
	char *dup;
	int i;

	i = 0;
	dup = (char*)malloc(sizeof(char*) + ft_strlen(s));
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
	free (dup);
}
